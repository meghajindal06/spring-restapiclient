package com.afkl.cases.df.airport.resource;


public class SelectionDetail {

	private Location origin;
	
	private Location dstination;
	
	private Fare fare;

	public Location getOrigin() {
		return origin;
	}

	public void setOrigin(Location origin) {
		this.origin = origin;
	}

	public Location getDstination() {
		return dstination;
	}

	public void setDstination(Location dstination) {
		this.dstination = dstination;
	}

	public Fare getFare() {
		return fare;
	}

	public void setFare(Fare fare) {
		this.fare = fare;
	}
	
	
	
	
}

